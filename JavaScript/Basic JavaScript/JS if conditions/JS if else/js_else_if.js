// JavaScript else

// Оператор else 
/*
Используйте оператор 'else' чтобы указать блок кода , который будет выполнен
, если условие равно false.
*/

let x = 5 , y = 7;
if (x > y) {
    console.log('x > y') // false
} else {
    console.log('y > x')
}

/* 
здесь x меньше y и поэтому наш первый условие неверное и роэтому 
она перехоит ко вторму 
*/