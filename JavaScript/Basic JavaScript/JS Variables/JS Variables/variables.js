// В программировании принято обявлять все переменные в наяале скрипта.


// Обявление переменной с помошью 'var' (не рекомендуется)

/*
Ключевое слово 'var' использовалось во всвем коде до 2015 
Ключевое слово 'let' и 'const' появилась в JavaScript в 2015 году.
*/

var x = 10;
var y = 5;
console.log('x * Y = ' + x*y)


// Когда использовать var , let , const

/*
1. Всегда обявляйте переменные 
2. Всегда используйте 'const' , если значение не должно меняться.
3. Всегда используйте 'const' если тип не должен меняться (массивы и обьекты)
4. Используйте let , только если нельзя испольpовать 'const'
5. Никогда не используйте 'var' , елси можно испольщовать let или const.
*/

