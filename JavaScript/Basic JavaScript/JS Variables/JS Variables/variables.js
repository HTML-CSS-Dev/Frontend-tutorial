// Идентификаторы JavaScript

/*
Переменные обозначаются уникальными именами , которые называются идентификаторами

Имена могут быть короткими , например x , y , z.

Имена могут быть описательными , например  age,sum,carName.

Правиа создания имен (идентификаторов):

    - Имена могут содержать буквы , цифры , символы , подчеркивания и знаки доларра.
    - Имена должны начинаться с буквы , знака долларра или символа подчеркивания (_)
    - имена чувствительны к регистру (X отличатеся от x)
    - Зарегестрированные слова (ключевые слова JavaScript) нельзя использовать в 
      качестве имен.
*/ 

// Примечание 
/*
    Цифры не могут быть символов в именах.
    Таким образом , JavaScript может легко отличать тдентификаторов от чисел.
*/ 

// Подчеркивание в JavaScript (_)
/*
В JavaScript подчеркивание считается буквой

Идентификаторы содерджащие _ являются допустимыми именами переменных.
*/ 

let _ = 'Sarah';
console.log("my mother's name is " + _)

/* Среди профессионалных программистов принято начинать имя переменной с символа 
подчеркивания, чтобы обозначить ее как << приватную >>
*/

// Знак доллара в JavaScript $
/*
JavaScript также воспринимает знак доларра как букву.

Идентификаторы содержащие $ являются именами переменных:
*/

let $ = 62;
console.log('I have earned' + $ + "bucks for a weeek")

/* Использование сивола $ в JavaScript не очень п=распространено , но професиональные 
прораммисты часто испольщуют его в качестве псевдонима для основной функции в 
библиотеках JavaScript.
*/


// Обявление переменной в JavaScirpt.
/*
Создание переменной в JavaScript называется обявлением пермеенной.

Переменные обявляются с ключевыми словами let или const.
*/ 


// Обявление переменной с помошью let

let car_name ;

/*
После обявления переменных не имеет значения (технически это 'undefined').

Чтобы присвоить пермеенной значение , испольщуйте знак равентсва(знак присваивания):
*/

car_name = "Volvo";
console.log('I have a ' + car_name + ' car')


// Чаще всего переменной присватвают значения при ее обявлении:
let _first_name = 'Shoxzod';
console.log('My first name is ' + _first_name)



// Обявление перменной с помошью 'const'
// Всегда испольщуйте 'const' если значение не должно изменяться.

const $last_name = 'Oripov';

console.log('My lastname is ' + $last_name)

// Автоматическое обявление перменной (не рекомендуется испольщовать)
