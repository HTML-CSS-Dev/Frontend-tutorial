// Оператор =
/* Простой оператор присваивания присваивает переменно простое значение */

let x = 3;
console.log('x = ' + x)

// Оператор += 
/* Оператор присваивания с добавлением добавляет значение к переменной. */

let y = 1;
y += 5;
console.log('y = ' + y)

// Оператор -=
/* Опертор вычитания при присваивания вычитает значение из переменной */

let z = 3;
z -= 1;
console.log('z = ' + z)

// Оператор *= 
/* Оператор присванивания с умножением умножает переменную */

let m = 6;
m *=2;
console.log('m = ' + m)


// Оператор **=
/* Оператор возведения в степень возводит переменную в степень операнда. */

let p = 5;
p **=2;
console.log('p = ' + p)

// Оператор /= 
/* Оператор присваивания с делением делит переменную. */

let q = 4;
q /= 2;
console.log('q = ' + q)

// Оператор %= 
/*
Оператор присванивания остатка присваивает остаток переменной.
*/

