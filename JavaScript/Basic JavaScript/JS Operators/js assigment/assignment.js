// Логические операторы

// Оператор &&
/* оператор И -  все варианты значений должны совпадать и тогда вывведится true*/ 
let k = 7; l = 8 , t =10;

let logic = k < t && l > k && t > l;
console.log('logic = ' + logic) //true

// елси одно из них не будет совпадать по логике ответ выявится "false"

logic = k < t && l < k && t > l;
console.log('logic = ' + logic) // false



// Оператор ||
/* Оператор || ИЛИ выбирает одно из правильных ответов если их есть 
если нет значение выводит false
*/

let num1 = 12,num2 =34;

let answer = num1 == num2 || num1 < num2;  // второе значение правильно
console.log('answer is ' + answer) //true

// если все значения неправильны то false.
answer = num1 == num2 || num1 > num2;
console.log('answer is ' + answer) // false